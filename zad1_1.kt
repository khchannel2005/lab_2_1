// Дано два списки чисел, як би ви повернули всі пари чисел? Наприклад, враховуючи список [1, 2, 3] і список [3, 4], ви повинні повернути [(1, 3), (1, 4), (2, 3), (2, 4), (3, 3), (3, 4)].

fun main() {
    val list1 = listOf(1, 2, 3)
    val list2 = listOf(3, 4)
    
    val pairs = list1.flatMap { num1 -> 
        list2.map { num2 -> 
            arrayOf(num1, num2) // Формуємо масив з двох елементів
        }
    }
    
    pairs.forEach { println(it.contentToString()) } // Виводимо кожну пару
}


// Виведе:
[1, 3]
[1, 4]
[2, 3]
[2, 4]
[3, 3]
[3, 4]
